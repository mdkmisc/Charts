<?php

// Get the max / min index
$max = 0;
$min = $this->values ? $this->values[0] : 0;
foreach ($this->values as $dta) {
    if ($dta > $max) {
        $max = $dta;
    } elseif ($dta < $min) {
        $min = $dta;
    }
}
$graph = "
    <script type='text/javascript'>
        $(function () {

            // Initiate the chart
            $('#$this->id').highcharts('Map', {

                "; if (!$this->responsive) {
    $graph .= "
                    chart: {
                        width: $this->width,
                        height: $this->height,
                    },";
}
                $graph .= "

                title : {
                    text : \"$this->title\"
                },

                mapNavigation: {
                    enabled: true,
                    enableDoubleClickZoomTo: true
                },

                colorAxis: {
                    min: $min,
                    "; if ($this->colors and count($this->colors) >= 2) {
                    $graph .= 'minColor: "'.$this->colors[0].'",';
                } $graph .= "
                    max: $max,
                    "; if ($this->colors and count($this->colors) >= 2) {
                    $graph .= 'maxColor: "'.$this->colors[1].'",';
                } $graph .= '
                },

                series : [{
                    data : [';
                      $i = 0;
                      foreach ($this->values as $dta) {
                          $e = $this->labels[$i];
                          $v = $dta;
                          $graph .= "{'code': \"$e\", 'value': $v},";
                          $i++;
                      }
                      $graph .= "
                    ],
                    mapData: Highcharts.maps['custom/world'],
                    joinBy: ['iso-a2', 'code'],
                    name: \"$this->element_label\",
                    states: {
                        hover: {
                            color: '#BADA55'
                        }
                    },
                }]
            });
        });
    </script>
    <div id='$this->id'></div>
";

return $graph;
